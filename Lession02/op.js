let a = 3, b = 2;
let z = a + b;


//incremento
//pre-incremento (el oprador ++ antes de la variable)

z = ++a;

console.log(a);
console.log(z);

//post-incremento (el oprador ++ despues de la variable)
z = b++;

console.log(b);
console.log(z);

//decremento
//predecremento

z = --a;
console.log(a);
console.log(z);

//post-decremento
z = b--;
console.log(b);
console.log(z);
